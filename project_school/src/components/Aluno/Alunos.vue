<template>
  <div>
    <titulo texto="Aluno"/>
    <div>     
      <input
        type="text"
        placeholder="Nome do Aluno"
        v-model="nome"
        v-on:keyup.enter="addAluno()"
      />
      <button class="btn btnInput" @click="addAluno()">
        Inserir

      </button>


    </div>

    <table>
      <thead>
        <th>Mat</th>
        <th>Nome</th>
        <th>Op</th>
      </thead>
      <tbody v-if="alunos.length">
        <tr v-for="(aluno, index) in alunos" :key="index">
          <td>{{aluno.id}}</td>
         <!-- <td>{{ index + 1}}</td>-->
          <td>{{ aluno.nome }} {{aluno.sobrenome}} </td>
          <td>
            <button class="btn btndanger" @click="remover(aluno)">
              Remover
            </button>
          </td>
        </tr>
      </tbody>
      <tfoot v-else>
         Nenhum aluno encontrado        
      </tfoot>
    </table>
  </div>
</template>

<script>
import Titulo from  '../_share/Titulo';

export default {
 components:{
   Titulo
 },

  data() {
    return {
      titulo: "Aluno",
      nome: "Alunos",
      alunos: [
        // { id: 1, nome: "Marcus", sobrenome: "Borba"  },
        // { id: 2, nome: "Ze", sobrenome: "Cesar" },
        // { id: 3, nome: "JoÃ£o", sobrenome: "Silva" },
      ],
    };
  },

created() {
  this.$http.get('http://localhost:3000/alunos')
  .then(res => res.json())
  .then(alunos => this.alunos = alunos )
},

  props: {},
  methods: {
    addAluno() {
      let _aluno = {
        nome: this.nome,
        sobrenome: ""
      }

     this.$http.post('http://localhost:3000/alunos', _aluno)
     .then(res => res.json())
     .then(alunoRetornado =>{
       this.alunos.push(alunoRetornado);
       this.nome;
     } )     

    //  this.alunos.push(_aluno);
    //  console.log("----------");
    //  this.nome = "";
    },
    remover(aluno) {
      

     this.$http.delete(`http://localhost:3000/alunos/${aluno.id}`)
     .then(() => {       
       let indice = this.alunos.indexOf(aluno);
       this.alunos.splice(indice, 1);     
    })
  }
}
};
</script>

